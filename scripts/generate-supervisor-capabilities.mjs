#!/usr/bin/env node
/**
 * generate-supervisor-capabilities.mjs
 *
 * Reads every YAML file in docs/capabilities/, validates the schema,
 * and emits apps/supervisor/src/capabilities.generated.ts.
 *
 * Run: node scripts/generate-supervisor-capabilities.mjs
 * Wired as part of "prebuild" in apps/supervisor/package.json
 */

import { readFileSync, readdirSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { createRequire } from 'module';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

const require = createRequire(join(ROOT, 'apps', 'supervisor', 'package.json'));
const { load: yamlLoad } = require('js-yaml');

const CAPS_DIR = join(ROOT, 'docs', 'capabilities');
const OUT_FILE = join(ROOT, 'apps', 'supervisor', 'src', 'capabilities.generated.ts');

const VALID_SIDE_EFFECTS = new Set(['none', 'read-external', 'write-app', 'write-external']);
const VALID_TIERS = new Set(['green', 'yellow', 'red']);

function validate(parsed, file) {
  const errors = [];
  if (!parsed.app || typeof parsed.app !== 'string') errors.push('missing or invalid `app`');
  if (!parsed.base_url) errors.push('missing `base_url`');
  if (!parsed.capabilities || !Array.isArray(parsed.capabilities)) errors.push('missing or invalid `capabilities`');
  for (const cap of (parsed.capabilities ?? [])) {
    if (!cap.id) errors.push(`capability missing \`id\``);
    if (!cap.side_effects) errors.push(`capability "${cap.id}" missing \`side_effects\``);
    else if (!VALID_SIDE_EFFECTS.has(cap.side_effects)) errors.push(`capability "${cap.id}" has invalid side_effects "${cap.side_effects}"`);
    if (!cap.required_scope) errors.push(`capability "${cap.id}" missing \`required_scope\``);
  }
  for (const tier of (parsed.tiers_allowed ?? [])) {
    if (!VALID_TIERS.has(tier)) errors.push(`invalid tier "${tier}" in tiers_allowed`);
  }
  if (errors.length > 0) {
    throw new Error(`[${file}] validation failed:\n  - ${errors.join('\n  - ')}`);
  }
}

// ── Main ─────────────────────────────────────────────────────────────────────

const files = readdirSync(CAPS_DIR).filter((f) => f.endsWith('.yml')).sort();

if (files.length === 0) {
  console.error(`[ERROR] No YAML files found in ${CAPS_DIR}`);
  process.exit(1);
}

const apps = [];
const errors = [];

for (const file of files) {
  const fullPath = join(CAPS_DIR, file);
  try {
    const raw = readFileSync(fullPath, 'utf8');
    const parsed = yamlLoad(raw);
    validate(parsed, file);
    apps.push(parsed);
    console.log(`  ✓ ${file} → app=${parsed.app} capabilities=${parsed.capabilities.length}`);
  } catch (e) {
    errors.push(e.message);
    console.error(`  ✗ ${file}: ${e.message}`);
  }
}

if (errors.length > 0) {
  console.error(`\n[ERROR] ${errors.length} capabilities file(s) failed validation.`);
  process.exit(1);
}

const generated = `// AUTO-GENERATED by scripts/generate-supervisor-capabilities.mjs
// DO NOT EDIT DIRECTLY — edit docs/capabilities/*.yml instead,
// then run: node scripts/generate-supervisor-capabilities.mjs
//
// Generated: ${new Date().toISOString()}
// Source files: ${files.join(', ')}

export interface Capability {
  id: string;
  desc?: string;
  route: string;
  side_effects: 'none' | 'read-external' | 'write-app' | 'write-external';
  required_scope: string;
  idempotency_key?: string;
  reversibility?: string;
  extra_guard?: string;
}

export interface AppCapabilities {
  app: string;
  repo: string;
  base_url: string;
  auth: string;
  supervisor_access: string;
  tiers_allowed: string[];
  capabilities: Capability[];
}

export const GENERATED_CAPABILITIES: AppCapabilities[] = ${JSON.stringify(apps, null, 2)};

export function getAppCapabilities(appId: string): AppCapabilities | undefined {
  return GENERATED_CAPABILITIES.find((a) => a.app === appId);
}

export function getCapability(appId: string, capId: string): Capability | undefined {
  return getAppCapabilities(appId)?.capabilities.find((c) => c.id === capId);
}
`;

writeFileSync(OUT_FILE, generated, 'utf8');
console.log(`[OK] Wrote ${apps.length} apps → ${OUT_FILE.replace(ROOT, '.')}`);
