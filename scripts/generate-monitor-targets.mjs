#!/usr/bin/env node
/**
 * FRH-05: Generate synthetic monitor liveness targets from docs/service-registry.yml.
 *
 * Output: apps/synthetic-monitor/src/targets.generated.ts
 *
 * Run:
 *   node scripts/generate-monitor-targets.mjs           # write file
 *   node scripts/generate-monitor-targets.mjs --check  # verify file is up-to-date (CI mode)
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const CHECK_MODE = process.argv.includes('--check');
const REGISTRY_PATH = resolve('docs/service-registry.yml');
const OUTPUT_PATH = resolve('apps/synthetic-monitor/src/targets.generated.ts');

// Workers excluded from liveness monitoring:
// - synthetic-monitor: self-check causes conflicts
// - admin-studio-production: production tier is monitored separately via custom domain
// - videoking: reference-only surface (no guaranteed health endpoint uptime)
const EXCLUDED_IDS = new Set(['synthetic-monitor', 'admin-studio-production', 'videoking']);

const registryRaw = readFileSync(REGISTRY_PATH, 'utf8');

/**
 * Parse worker entries from the service-registry.yml workers section.
 * Uses regex — the YAML structure is regular enough to avoid a full parser.
 */
function parseWorkers(yaml) {
  const workers = [];
  // Split on worker block boundaries: lines starting with "  - id:"
  const blockPattern = /^  - id:\s*(\S+)/gm;
  let match;
  while ((match = blockPattern.exec(yaml)) !== null) {
    const id = match[1];
    const start = match.index;
    // Find the start of the next block or end of section
    blockPattern.lastIndex = start + 1;
    const next = blockPattern.exec(yaml);
    const end = next ? next.index : yaml.length;
    blockPattern.lastIndex = next ? next.index : yaml.length;

    const block = yaml.slice(start, end);

    // Only parse workers (not pages or packages)
    const urlMatch = block.match(/^\s+url:\s*(https?:\/\/[^\s#]+)/m);
    const healthMatch = block.match(/^\s+health_endpoint:\s*(\S+)/m);

    if (!urlMatch || !healthMatch) continue;

    workers.push({
      id,
      url: urlMatch[1].trim(),
      healthEndpoint: healthMatch[1].trim(),
    });
  }
  return workers;
}

const workers = parseWorkers(registryRaw).filter((w) => !EXCLUDED_IDS.has(w.id));

const targets = workers.map((w) => ({
  id: `${w.id}.health`,
  url: `${w.url}${w.healthEndpoint}`,
  contains: 'ok',
}));

const lines = [
  '// DO NOT EDIT — generated by scripts/generate-monitor-targets.mjs',
  '// Run: node scripts/generate-monitor-targets.mjs',
  '',
  'export const GENERATED_TARGETS = [',
  ...targets.map(
    (t) =>
      `  { id: ${JSON.stringify(t.id)}, url: ${JSON.stringify(t.url)}, contains: ${JSON.stringify(t.contains)} },`,
  ),
  '] as const;',
  '',
];

const output = lines.join('\n');

if (CHECK_MODE) {
  let existing;
  try {
    existing = readFileSync(OUTPUT_PATH, 'utf8');
  } catch {
    console.error(`ERROR: ${OUTPUT_PATH} does not exist. Run: node scripts/generate-monitor-targets.mjs`);
    process.exit(1);
  }
  if (existing !== output) {
    console.error(`ERROR: ${OUTPUT_PATH} is stale. Run: node scripts/generate-monitor-targets.mjs`);
    console.error('Diff:');
    const existingLines = existing.split('\n');
    const newLines = output.split('\n');
    const maxLen = Math.max(existingLines.length, newLines.length);
    for (let i = 0; i < maxLen; i++) {
      if (existingLines[i] !== newLines[i]) {
        console.error(`  line ${i + 1} expected: ${JSON.stringify(newLines[i] ?? '')}`);
        console.error(`  line ${i + 1}   actual: ${JSON.stringify(existingLines[i] ?? '')}`);
      }
    }
    process.exit(1);
  }
  console.log(`Monitor targets check passed (${targets.length} liveness probes from service-registry.yml).`);
} else {
  writeFileSync(OUTPUT_PATH, output, 'utf8');
  console.log(`Generated ${targets.length} liveness probe targets → ${OUTPUT_PATH}`);
}
