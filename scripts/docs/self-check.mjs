#!/usr/bin/env node

import { execFileSync } from 'node:child_process';
import { existsSync, readFileSync, readdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import yaml from 'js-yaml';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const ROOT = process.env.DOCS_TARGET_ROOT ? resolve(process.env.DOCS_TARGET_ROOT) : resolve(SCRIPT_DIR, '..', '..');
const GRAPH_PATH = join(ROOT, 'docs', '_catalog', 'docs-graph.json');
const CANONICAL_PATH = join(ROOT, 'docs', '_governance', 'canonical-docs.yml');
const GENERATED_FILES = [
  'docs/CATALOG.md',
  'docs/CANONICAL_DOCS.md',
  'docs/STALE_DOCS.md',
  'docs/OWNER_INDEX.md',
  'docs/_catalog/docs-graph.json',
];

function generatedDiagramFiles() {
  const dir = join(ROOT, 'docs', '_generated');
  if (!existsSync(dir)) return [];
  return readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
    .map((entry) => `docs/_generated/${entry.name}`);
}

function currentCommit() {
  try {
    return execFileSync('git', ['rev-parse', 'HEAD'], { cwd: ROOT, encoding: 'utf8' }).trim();
  } catch {
    return null;
  }
}

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

function fail(checks, id, message, fix) {
  checks.push({ id, severity: 'error', message, fix });
}

function warn(checks, id, message, fix) {
  checks.push({ id, severity: 'warning', message, fix });
}

function main() {
  const checks = [];

  if (!existsSync(GRAPH_PATH)) {
    fail(checks, 'docs.self.graph-missing', 'docs/_catalog/docs-graph.json is missing.', 'Run npm run docs:catalog.');
    report(checks);
    process.exit(1);
  }

  const graph = readJson(GRAPH_PATH);
  const commit = currentCommit();

  if (graph.generated_by !== 'scripts/docs/catalog.mjs') {
    fail(checks, 'docs.self.bad-generator', 'docs graph was not generated by scripts/docs/catalog.mjs.', 'Regenerate with npm run docs:catalog.');
  }

  if (commit && graph.generated_at_commit && graph.generated_at_commit !== commit) {
    warn(checks, 'docs.self.commit-drift', 'docs graph was generated from a different commit.', 'Run npm run docs:catalog on the current commit.');
  }

  for (const repoPath of GENERATED_FILES) {
    if (!existsSync(join(ROOT, repoPath))) {
      fail(checks, 'docs.self.generated-missing', `${repoPath} is missing.`, 'Run npm run docs:catalog.');
    }
  }

  for (const repoPath of [...GENERATED_FILES, ...generatedDiagramFiles()].filter((path) => path.endsWith('.md'))) {
    const text = readFileSync(join(ROOT, repoPath), 'utf8');
    if (!text.startsWith('<!-- GENERATED FILE.')) {
      fail(checks, 'docs.self.generated-header-missing', `${repoPath} is missing the generated-file header.`, 'Regenerate with npm run docs:catalog.');
    }
  }

  const canonicalConfig = yaml.load(readFileSync(CANONICAL_PATH, 'utf8')) ?? {};
  const allowlisted = new Set((canonicalConfig.canonical_docs ?? []).map((entry) => entry.path));
  const canonicalDocs = graph.docs.filter((doc) => doc.status === 'canonical');

  for (const doc of canonicalDocs) {
    if (!allowlisted.has(doc.path)) {
      fail(checks, 'docs.self.canonical-not-allowlisted', `${doc.path} is canonical in graph but not allowlisted.`, 'Update docs/_governance/canonical-docs.yml or fix status inference.');
    }
    if (doc.fidelity === 'historical') {
      fail(checks, 'docs.self.impossible-state', `${doc.path} is canonical with historical fidelity.`, 'Set the doc to archive or update fidelity after verification.');
    }
    if (!doc.owner || doc.owner === 'unknown') {
      fail(checks, 'docs.self.canonical-owner-missing', `${doc.path} has no concrete owner.`, 'Add an owner in docs/_governance/canonical-docs.yml or frontmatter.');
    }
  }

  for (const repoPath of allowlisted) {
    if (!graph.docs.some((doc) => doc.path === repoPath && doc.status === 'canonical')) {
      fail(checks, 'docs.self.allowlist-not-in-graph', `${repoPath} is allowlisted but not present as canonical in docs graph.`, 'Restore the file or update docs/_governance/canonical-docs.yml.');
    }
  }

  if (graph.errors?.length) {
    for (const error of graph.errors) {
      fail(checks, error.id ?? 'docs.self.graph-error', error.message ?? 'docs graph contains an error.', error.fix ?? 'Inspect docs/_catalog/docs-graph.json.');
    }
  }

  report(checks);
  process.exit(checks.some((check) => check.severity === 'error') ? 1 : 0);
}

function report(checks) {
  if (checks.length === 0) {
    console.log('[docs:self-check] PASS');
    return;
  }

  const errors = checks.filter((check) => check.severity === 'error');
  const warnings = checks.filter((check) => check.severity === 'warning');
  console.log(`[docs:self-check] ${errors.length} error(s), ${warnings.length} warning(s)`);
  for (const check of checks) {
    const prefix = check.severity === 'error' ? 'ERROR' : 'WARN';
    console.log(`[${prefix}] ${check.id}: ${check.message}`);
    if (check.fix) console.log(`       fix: ${check.fix}`);
  }
}

main();
